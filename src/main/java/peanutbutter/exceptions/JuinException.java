package peanutbutter.exceptions;

/**
 * Represents an exception specific to the Juin application.
 */
public class JuinException extends Exception {

    /**
     * Creates a new {@code JuinException} with the specified error message.
     *
     * @param e The detail message for this exception.
     */
    public JuinException(String e) {
        super(e);
    }
}
